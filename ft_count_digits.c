#include "libft.h"

int	ft_count_digits(int n)
{
	int i;
	double cur;

	i = 0;
	ft_pop_sign(&n);
	cur = n;
	while (cur > 1)
	{
		cur /= 10;
		i++;
	}
	return (i);
}
